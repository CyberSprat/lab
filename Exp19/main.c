#include <stdio.h>
#include <stdlib.h>

int main(){
    printf("Enter a day(1-31):");
    int day=0;
    scanf("%d", &day);
    if(day>=1 && day<=31){
        for(int i=day; i<=31; i+=2){
            if(i==7 || i==14 || i==21)
                i++;
            printf("In %d-th day - Math\n", i);
        }
    }
    else
        exit(0);
    return 0;
}
